# 숫자
number = 3
print(type(number))
# 문자
string = 'hello!!'
print(type(string))
# boolean (True,False)
boolean = True
print((type(boolean)))

# 형변환
string_number = '5'
number = int(string_number)
print(number + 5)

# f-string / string interpolation
name = 'eric'
print(f'안녕하세요. {name}입니다. 반갑습니다.')
# string interpolation --> 문자 안에 변수를 넣는 행위

